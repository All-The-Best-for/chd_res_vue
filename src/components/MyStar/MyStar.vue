<template>
    <div class="star" :class="'star-'+size">
    <span class="star-item" v-for="(starClass,index) in starClasses" :key="index" :class="starClass"></span>
</div>
</template>

<script>
export default {
    name:'MyStar',
    props:{
        score:Number,
        size:Number,
    },
    computed: {
        // 麻烦一点，但没有警告
        starClasses(){
            /* 
            3.2:3+0+2
            3.6:3+1+1
            */
            const CLASS_ON='on'
            const CLASS_HALF='half'
            const CLASS_OFF='off'
            const {score}=this
            const scs=[]
            //向scs中添加若干个‘on’
            const scoreInteger=Math.floor(score)
            for (let i = 0; i < scoreInteger; i++) {
                scs.push(CLASS_ON)
            }
            //向scs中添加1个或0个‘half’
            let halfNum=0
            if(score-scoreInteger>=0.5){
                scs.push(CLASS_HALF)
                halfNum=1
            }
            //向scs中添加若干个‘off’
            const offNum=5-scoreInteger-halfNum
            for (let i = 0; i <offNum; i++) {
                scs.push(CLASS_OFF)             
            }
            return scs
        },    }
}
</script>

